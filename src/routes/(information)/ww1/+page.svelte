<h3 class="heading-small">What was the Schlieffen Plan?</h3>
<p class="body">
  The Schlieffen Plan was a German pre-war plan that intended to defeat France first, before Russia could fully mobilise its forces.
  The main German forces were to mass on the extreme right, advancing through neutral Belgium to encircle French armies.
  This was supposed to avoid the stronger French defences along the German border.
  German forces would then envelop the French armies, crushing them against the German southern wing.
  The plan called for a 7 to 1 manpower ratio from the right wing to the left, ensuring a massive force for the enveloping manoeuvre.
  Schlieffen was proposed by Alfred Graf von Schlieffen, Chief of the German General Staff.
  He also developed a detailed timetable accounting for potential French responses to German actions.
  It was altered prior to its implementation in WWI by Helmuth von Moltke.
  Moltke reduced the size of the attacking army, a change often blamed for Germany's failure to achieve a quick victory.
</p>
